#include "holberton.h"
/**
  * print_diagonal - Entry Point
  * @n: number of times print '_'
  * Return: 0 (Success)
  */
void print_diagonal(int n)
{
	int i, j;

	if (n <= 0)
	{
		_putchar('\n');
		return;
	}

	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
			_putchar(' ');
		_putchar('\\');
		_putchar('\n');
	}
}
